package com.idat.MarioAlejoTapullimaOpenFeign.modelo;

public class Lavadora {
	
	private Integer idLavadora;
	private String descripcionLavadora;
	private String marcaLavadora;
	private Integer voltajeLavadora;
	public Integer getIdLavadora() {
		return idLavadora;
	}
	public void setIdLavadora(Integer idLavadora) {
		this.idLavadora = idLavadora;
	}
	public String getDescripcionLavadora() {
		return descripcionLavadora;
	}
	public void setDescripcionLavadora(String descripcionLavadora) {
		this.descripcionLavadora = descripcionLavadora;
	}
	public String getMarcaLavadora() {
		return marcaLavadora;
	}
	public void setMarcaLavadora(String marcaLavadora) {
		this.marcaLavadora = marcaLavadora;
	}
	public Integer getVoltajeLavadora() {
		return voltajeLavadora;
	}
	public void setVoltajeLavadora(Integer voltajeLavadora) {
		this.voltajeLavadora = voltajeLavadora;
	}
	
}
